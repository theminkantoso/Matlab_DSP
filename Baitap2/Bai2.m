y2 = zeros(1,length(n));
y1((n>=min(n1))& (n<=max(n1))==1) = u1;
y2((n>=min(n2)) & (n<=max(n2))==1) = u2;
y = y1 .* y2;
subplot(4,1,1);
stem(n,y1);
subplot(4,1,2);
stem(n,y2);
subplot(4,1,3);
stem(n,y);
